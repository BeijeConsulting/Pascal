package it.beije.pascal.XMLParser2;

public class DocumentElement {
	private String nome;
	private String valore;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getValore() {
		return valore;
	}
	public void setValore(String valore) {
		this.valore = valore;
	}	
}
